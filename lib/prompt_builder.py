# ── Anchor Prompt (DNA) ──────────────────────────────────────
ANCHOR_EN = (
    "Cinematic 4K quality, photorealistic detail, professional film production, "
    "high fidelity textures, smooth natural motion, elegant composition, "
    "no AI artifacts, no morphing, no visual glitches."
)


def build_prompts(data: dict) -> dict:
    video_type = data.get("video_type", "")
    goal = data.get("goal", "")
    mood = data.get("mood", "")
    subject = data.get("subject", "")
    environment = data.get("environment", "")
    action = data.get("action", "")
    scene_feel = data.get("scene_feel", "")
    lighting = data.get("lighting", "")
    camera_movement = data.get("camera_movement", "")
    shot_type = data.get("shot_type", "")
    depth_of_field = data.get("depth_of_field", "")
    pace = data.get("pace", "")
    fixed_elements = data.get("fixed_elements", [])
    avoid_extra = data.get("avoid_extra", "")
    ref_character = data.get("ref_character")
    ref_lighting = data.get("ref_lighting")
    ref_camera = data.get("ref_camera")
    ref_audio = data.get("ref_audio")

    # ── English prompt (flowing directing instructions) ────────
    video_type_en = VIDEO_TYPE_MAP.get(video_type, video_type)
    goal_en = GOAL_MAP.get(goal, goal)
    mood_en = MOOD_MAP.get(mood, mood)
    mood_feel = MOOD_FEEL_MAP.get(mood, mood)
    lighting_en = LIGHTING_MAP.get(lighting, lighting)
    camera_en = CAMERA_MAP.get(camera_movement, camera_movement)
    shot_en = SHOT_MAP.get(shot_type, shot_type)
    dof_en = DOF_MAP.get(depth_of_field, depth_of_field)
    pace_en = PACE_MAP.get(pace, pace)

    fixed_en_list = [FIXED_EN.get(f, f) for f in fixed_elements]
    fixed_en_str = ", ".join(fixed_en_list) if fixed_en_list else "overall visual consistency"

    avoid_en_list = list(AVOID_EN.values())
    if avoid_extra:
        avoid_en_list.append(avoid_extra)
    avoid_en_str = ", ".join(avoid_en_list)

    # Build flowing prompt like a director's instructions
    parts = []

    # Scene description
    scene = f"Create {video_type_en} featuring {subject} in {environment}"
    if action:
        scene += f", {action}"
    scene += "."
    parts.append(scene)

    # Lighting and atmosphere
    parts.append(f"The scene is lit with {lighting_en}, creating a {mood_en} atmosphere.")

    # Camera and framing
    cam_sentence = f"Camera: {camera_en}, framed as a {shot_en} with {dof_en}."
    parts.append(cam_sentence)

    # Pacing
    parts.append(f"The pacing is {pace_en}.")

    # Scene feel
    if scene_feel:
        parts.append(f"The final result should feel like {scene_feel}.")
    else:
        parts.append(f"The final result should feel like {mood_feel} — {goal_en}.")

    # References
    if ref_character and ref_character.get("url"):
        desc = ref_character.get("description", "")
        parts.append(f"Character/product reference provided — maintain exact appearance throughout. {desc}".strip())
    if ref_lighting and ref_lighting.get("url"):
        desc = ref_lighting.get("description", "")
        parts.append(f"Match the lighting style from the provided reference. {desc}".strip())
    if ref_camera and ref_camera.get("url"):
        desc = ref_camera.get("description", "")
        parts.append(f"Replicate the camera movement style from the provided reference. {desc}".strip())
    if ref_audio and ref_audio.get("url"):
        desc = ref_audio.get("description", "")
        parts.append(f"Synchronize the scene rhythm and pacing with the provided audio reference. {desc}".strip())

    # Consistency
    parts.append(f"Keep consistent throughout: {fixed_en_str}.")

    # Avoid
    parts.append(f"Avoid: {avoid_en_str}.")

    # Anchor (quality DNA)
    parts.append(ANCHOR_EN)

    english_prompt = " ".join(parts)

    # ── Arabic prompt ─────────────────────────────────────────
    video_type_ar = VIDEO_TYPE_AR.get(video_type, video_type)
    goal_ar = GOAL_AR.get(goal, goal)
    mood_ar = MOOD_AR.get(mood, mood)
    lighting_ar = LIGHTING_AR.get(lighting, lighting)
    camera_ar = CAMERA_AR.get(camera_movement, camera_movement)
    shot_ar = SHOT_AR.get(shot_type, shot_type)

    fixed_ar_list = [FIXED_AR.get(f, f) for f in fixed_elements]
    fixed_ar_str = "، ".join(fixed_ar_list) if fixed_ar_list else "التناسق البصري العام"

    avoid_ar_list = list(AVOID_AR.values())
    if avoid_extra:
        avoid_ar_list.append(avoid_extra)
    avoid_ar_str = "، ".join(avoid_ar_list)

    ar_parts = []
    ar_scene = f"{video_type_ar} يعرض {subject} في {environment}"
    if action:
        ar_scene += f"، {action}"
    ar_scene += "."
    ar_parts.append(ar_scene)

    ar_parts.append(f"{lighting_ar}، أجواء {mood_ar}.")
    ar_parts.append(f"الكاميرا: {camera_ar}، {shot_ar}.")

    if scene_feel:
        ar_parts.append(f"الإحساس النهائي: {scene_feel}.")
    else:
        ar_parts.append(f"الهدف: {goal_ar}.")

    if ref_character and ref_character.get("url"):
        ar_parts.append("مرجع الشخصية/المنتج مرفق — الحفاظ على المظهر الدقيق.")
    if ref_lighting and ref_lighting.get("url"):
        ar_parts.append("مرجع الإضاءة مرفق — مطابقة أسلوب الإضاءة.")
    if ref_camera and ref_camera.get("url"):
        ar_parts.append("مرجع حركة الكاميرا مرفق.")
    if ref_audio and ref_audio.get("url"):
        ar_parts.append("مرجع صوتي مرفق — مزامنة الإيقاع.")

    ar_parts.append(f"الثوابت: {fixed_ar_str}.")
    ar_parts.append(f"تجنب: {avoid_ar_str}.")

    arabic_prompt = "\n".join(ar_parts)

    return {
        "arabic_prompt": arabic_prompt.strip(),
        "english_prompt": english_prompt.strip(),
    }
